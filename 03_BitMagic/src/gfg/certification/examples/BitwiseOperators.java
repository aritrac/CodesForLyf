package gfg.certification.examples;

/**
 * In this example we will see the three bitwise operators 'and' 'or' 'xor'
 */

public class BitwiseOperators {
    public static void main(String[] args) {
        int x = 3, y =6;
        System.out.println("x & y = " + (x & y));
        System.out.println("x & y = " + (x | y));
        System.out.println("x & y = " + (x ^ y));
    }
}
